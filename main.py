print("환영합니다! 앞으로 신조어와 관련된 3가지 퀴즈를 낼테니 맞추어보세요")  
  
# 문제 순서(인덱스)  
sequence = 0  
# 정답 개수  
t = 0  
# 오답 개수  
f = 0  
  
# 신조어 퀴즈 리스트  
qs = ["취존", "솔까말", "케바케"]  
# 각 신조어의 정답(풀어쓴 문장)  
aw = ["취향존중", "솔직히까놓고말해서", "케이스바이케이스"]  
  
# 총 3문제 반복  
while sequence < 3:  
    # 사용자에게 신조어의 뜻을 입력받음  
    q1 = input(qs[sequence] + "이 어떤 문장의 줄임말인가요?:")  
    # 입력값에서 공백 제거  
    a1 = q1.replace(" ", "")  
    # 정답 판정  
    if a1 == aw[sequence]:  
        t = t + 1  
        print("정답입니다")  
    else:  
        f = f + 1  
        print("오답입니다")  
    sequence = sequence + 1  
  
# 결과 출력  
t = str(t)  
f = str(f)  
print("맞은 개수는 " + t + "개, 틀린 개수는 " + f + "개 입니다")  
